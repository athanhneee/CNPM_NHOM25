    if (!lecturerSchedule.has(lecturerId)) lecturerSchedule.set(lecturerId, []);
    const lecSched = lecturerSchedule.get(lecturerId)!;

    let weekday = WEEKDAYS[0];
    let startPeriod = PERIOD_SLOTS[0];
    let roomObj = ROOMS[0];

