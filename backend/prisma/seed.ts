  // 3 non-overlapping time blocks per day: startPeriod 1 (Ca sáng), 5 (Ca chiều), 9 (Ca tối)
  // 6 weekdays (Mon–Sat, encoded 2–7) × 3 blocks = 18 slots per lecturer / room
  // 14 rooms × 18 slots = 252 total capacity (>160 courses)
  const WEEKDAYS = [2, 3, 4, 5, 6, 7];
  const PERIOD_SLOTS = [1, 5, 9];

  type SlotEntry = {
    weekday: number;
    startPeriod: number;
    periodCount: number;
  };

  function scheduleConflicts(
    schedule: SlotEntry[],
    weekday: number,
    startPeriod: number,
    periodCount: number,
  ): boolean {
    return schedule.some(
      (s) =>
        s.weekday === weekday &&
        s.startPeriod < startPeriod + periodCount &&
        startPeriod < s.startPeriod + s.periodCount,
    );
  }

