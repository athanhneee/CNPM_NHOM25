function resolveSectionStatus(
  template: Omit<Section, 'registeredCount' | 'waitlistCount'>,
  counts: { registeredCount: number; waitlistCount: number },
): Section['status'] {
  if (
    template.status === 'CANCELLED' ||
    template.status === 'COMPLETED' ||
    template.status === 'IN_PROGRESS' ||
    template.status === 'CLOSED'
  ) {
    return template.status
  }

  return counts.registeredCount >= template.capacity ? 'FULL' : 'OPEN'
}

